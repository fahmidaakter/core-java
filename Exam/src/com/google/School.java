package com.google;

/**
 *
 * @author J2EE-33
 */
public abstract  class School {
   String name;
   int rollNumber;

    public School(String name, int rollNumber) {
        this.name = name;
        this.rollNumber = rollNumber;
    }

    public String getName() {
        return name;
    }

    public int getRollNumber() {
        return rollNumber;
    }


    
}
